public class datatype{
	public static void main(String[] args) {
		System.out.println(2); // number
		System.out.println("two"); // String
		
		System.out.println("2"); // String 2
		System.out.println(2 + 2); // 4
		System.out.println("2" + "2"); // 22
		
		System.out.println(2 * 2); // 4
		//System.out.println("2" * "2"); 불가능
		System.out.println("2222".length()); // 4 (문자열 길이)
		//System.out.println(2222.length()); // 불가능
		
		
	}
}
